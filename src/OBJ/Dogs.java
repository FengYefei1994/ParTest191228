package OBJ;
//类是一种引用数据类型

public class Dogs {                    //描述dog对象共有特征和行为
	//属性、方法
	
	String name;                        //名
	String color;                       //颜色
	int age;                            //年龄
	
	void eat() {                               //吃方法
		System.out.println();
	}
	
	void sleep() {                             //睡方法
		System.out.println();
	}
	
	void beat() {                              //咬方法
		System.out.println();
	}
	
	static void show() {                       //静态方法
		System.out.println("摇尾巴");
	}
}
